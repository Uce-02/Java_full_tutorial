public class Chapter5_Expressions {

    public static void main (String[] args) {

        //expression = operands & operators
        //operand = values, variables, numbers, quantity
        //operators = + - / %

        int friends = 10;

        friends++; // increment 1. friends--; to decrement 1

        friends = friends / 3; //to make double. double friends = 10. friends = (double) friends / 3

        System.out.println(friends);

    }
}
